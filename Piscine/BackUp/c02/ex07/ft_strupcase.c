int		is_letter(char ch)
{
	if (ch <= 'z' && ch >= 'a')
		return (1);
	return (0);
}

char	*ft_strupcase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (is_letter(str[i]) == 1)
			str[i] -= 32;
		i++;
	}
	return (str);
}
