int		ft_op_add(int val1, int val2)
{
	return (val1 + val2);
}

int		ft_op_sub(int val1, int val2)
{
	return (val1 - val2);
}

int		ft_op_mul(int val1, int val2)
{
	return (val1 * val2);
}

int		ft_op_div(int val1, int val2)
{
	return (val1 / val2);
}

int		ft_op_mod(int val1, int val2)
{
	return (val1 % val2);
}
