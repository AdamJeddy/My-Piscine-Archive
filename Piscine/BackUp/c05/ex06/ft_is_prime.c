int		ft_is_prime(int nb)
{
	int i;
	int size_half;

	i = 3;
	size_half = nb / 2;
	if (nb == 2)
		return (1);
	if (nb <= 1 || nb % 2 == 0)
		return (0);
	while (i <= size_half)
	{
		if (nb % i == 0)
			return (0);
		i += 2;
	}
	return (1);
}
