#include <stdio.h>

int		ft_sqrt(int nb)
{
	int i;
	int half;

	i = 1;
	half = nb / 2;
	if (nb == 1)
		return (1);
	while (i <= half && i <= 46340)
	{
		if ((i * i) == nb)
			return (i);
		i++;
	}
	return (0);
}
